package sec06.exam01;

public class PrintExample {
	public static void main(String[] args) {
		
		System.out.println("하하"); // println은 자동으로 개행이 들어간다. 
		System.out.println("호호");
		
		System.out.println(); //sysout + ctrl + space : 단축키
		System.out.print("하하"); // 개행문자들 들어가지 않으므로 한 라인 안에서 출력된다.
		System.out.print("호호");
		System.out.println();
		System.out.println();
		System.out.print("하하\n"); // ""안에 \n을 넣으면 개행이 된다. 
		System.out.print("호호\n");
		
//		System.out.printf();
		
		
		
	}

}
